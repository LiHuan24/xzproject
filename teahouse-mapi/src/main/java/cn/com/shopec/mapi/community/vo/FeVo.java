package cn.com.shopec.mapi.community.vo;

public class FeVo {

	// 分类名称
	private String sortName;
	// 设备总数量
	private String feNumber;
	// 设备空闲数量
	private String freeNumber;

	public String getSortName() {
		return sortName;
	}

	public void setSortName(String sortName) {
		this.sortName = sortName;
	}

	public String getFeNumber() {
		return feNumber;
	}

	public void setFeNumber(String feNumber) {
		this.feNumber = feNumber;
	}

	public String getFreeNumber() {
		return freeNumber;
	}

	public void setFreeNumber(String freeNumber) {
		this.freeNumber = freeNumber;
	}

}
